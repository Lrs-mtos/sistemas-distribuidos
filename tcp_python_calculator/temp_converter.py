def celsius2kelvin(c):
    return c + 273

def kelvin2celsius(k):
    return k - 273

def kelvin2fahrenheit(k):
    return (k - 273) * 9/5 + 32

def fahrenheit2kelvin(f):
    return (f - 32) * 5/9 + 273

def celcius2fahrenheit(c):
    return c * 9/5 + 32

def fahrenheit2celcius(f):
    return (f - 32) * 5/9